from rest_framework import serializers

class EmployeeSalaryStatementSerializer(serializers.Serializer):
    companyId = serializers.IntegerField(required=False, allow_null=True)
    clockInOutId = serializers.IntegerField(required=False, allow_null=True)
    employeeId = serializers.IntegerField(required=False, allow_null=True)
    employeeName = serializers.CharField(required=False, allow_null=True)
    departmentId = serializers.IntegerField(required=False, allow_null=True)
    departmentName = serializers.CharField(required=False, allow_null=True)
    basicSalary = serializers.IntegerField(required=False, allow_null=True)
    totalEarnSalary = serializers.IntegerField(required=False, allow_null=True)
    overTime = serializers.IntegerField(required=False, allow_null=True)
    otAmount = serializers.IntegerField(required=False, allow_null=True)
    pfAmount = serializers.IntegerField(required=False, allow_null=True)
    totalPfAmount = serializers.IntegerField(required=False, allow_null=True)
    pfPercentage = serializers.IntegerField(required=False, allow_null=True)
    ptAmount = serializers.IntegerField(required=False, allow_null=True)
    totalEarnings = serializers.IntegerField(required=False, allow_null=True)
    otherDeductions = serializers.IntegerField(required=False, allow_null=True)
    totalPenaltyAmount = serializers.IntegerField(required=False, allow_null=True)
    totalDeductions = serializers.IntegerField(required=False, allow_null=True)
    netSalary = serializers.IntegerField(required=False, allow_null=True)
    totalPaidDays = serializers.IntegerField(required=False, allow_null=True)
    totalWorkingDays = serializers.IntegerField(required=False, allow_null=True)
    totalDays = serializers.IntegerField(required=False, allow_null=True)
    totalWorkingHours = serializers.FloatField(required=False, allow_null=True)
    employeeType = serializers.CharField(required=False, allow_null=True)
    totalAllowance = serializers.IntegerField(required=False, allow_null=True)
    deduction = serializers.IntegerField(required=False, allow_null=True)
    used_leave = serializers.IntegerField(required=False, allow_null=True)
